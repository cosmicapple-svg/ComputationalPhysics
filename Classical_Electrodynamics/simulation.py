# ## 1. Electrostatic Fields of Point Charges

# Having $N$ point charges $Q_i$ fixed at positions in the plane given by the vectors $\vec{r}_i$, $i=1,\ldots, N$. The electric field is given by Coulomb's Law:

# $$\vec{E}(\vec{r})=\frac{1}{4\pi\epsilon_0} \sum_{i=1}^N\dfrac{Q_i}{|\vec{r}-\vec{r}_i|^2} \hat{\rho}_i$$

# where $\hat{\rho}_i=(\vec{r}-\vec{r}_i)/|\vec{r}-\vec{r}_i|$ is the unit vector in the $\vec{r}-\vec{r}_i|$ direction. This field can be split into the components:

# $$\begin{aligned}
#     E_x(x,y)=\dfrac{1}{4\pi\epsilon_0}\sum_{i=1}^N \dfrac{Q_i(x-x_i)}{((x-x_i)^2+(y-y_i)^2)^{3/2}}\\
#     E_y(x,y)=\dfrac{1}{4\pi\epsilon_0}\sum_{i=1}^N \dfrac{Q_i(y-y_i)}{((x-x_i)^2+(y-y_i)^2)^{3/2}}
# \end{aligned}$$

# Furthermore, the field comes from a potential following the next relation:

# $$\vec{E}(\vec{r})=-\vec{\nabla}V(\vec{r})$$

# The electrostatic potential at a point $\vec{r}$ is

# $$V(\vec{r})=V(x,y)= \dfrac{1}{4\pi\epsilon_0} \sum_{i=1}^N \dfrac{Q_i}{((x-x_i)^2+(y-y_i)^2)^{1/2}}$$

# The electric field lines are the integral curves of the vectorial field $\vec{E}$. The electric field's magnitude is proportional to the density of field lines, i.e. the electric flux $\Phi_E=\int_{\mathrm{S}}\vec{E}\cdot d\vec{A}$ through a surface $\mathrm{S}$ is proportional to the number of field lines that cross the surface. The electric field lines of point charge distributions start at positive charges and finish at negative charges or extend to infinity.

# The equipotential surfaces are the union of points in space where the electrostatic potential has fixed values, and are closed surfaces. It can be shown that an intense electric field in a given point is equivalent to a great spatial variation of the electric potential, i.e., a high density of equipotential surfaces. The electric field is perpendicular to the equipotential surfaces at each point, and points in the direction where $V$ decreases.

