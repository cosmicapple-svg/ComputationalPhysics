# ## 3. Poisson's Equation

# The next step is to compute the potentials and fields in regions near charges, where the differential Gauss Law takes the form

# $$\vec{\nabla}\cdot \vec{E}(x,y,z)= \frac{\partial E_x}{\partial x} + \frac{\partial E_y}{\partial y} + \frac{\partial E_z}{\partial z}=\frac{\rho}{\epsilon_0}$$

# And we know that the electric field is the gradient of the electric potential. Which tells us that the electric potential obeys Poisson's Equation

# $$\vec{\nabla}^2V = \frac{\partial^2V}{\partial x^2} + \frac{\partial^2V}{\partial y^2} + \frac{\partial^2V}{\partial z^2}=-\frac{\rho}{\epsilon_0}$$

# where $\rho$ is the charge density and $\epsilon_0$ is the electric permitivity constant. The computational methods are similar, only a term for the charge density is added. For example, Jacobi's methos would be

# $$\begin{aligned}
#     V(i,j,k)= & \frac{1}{6}[V(i+1,j,k)+V(i-1,j,k) +V(i,j+1,k) +V(i,j-1,k) \\ &  +V(i,j,k+1)+V(i,j,k-1)] +\frac{\rho(i,j,k)(\Delta x)^2}{6\epsilon_0}
# \end{aligned}$$

# where the charge density, in general, is a function of the position.

# ### 3.1 Point Charge

# Considering a situation in which

# $$\rho=\left\{ \begin{array}{ll}
#     1, & x=y=0 \\
#     0, & \text{en otro caso}
# \end{array} \right.$$

# a code similar to those for Laplace equation can be developed.

