# ### 2.4 Convergence of Jacobi's Method

# Unfortunately, Jacobi's method of relaxation does not count with good convergence properties.

# Turns out that, when applied to a problem in the plane, for example to a $L\times L$ grid, the number of iterations required to reach convergence is of the order of $L^2$. This is bad because if we augment the number of points by a factor of 2, convergence will require 4 times more iterations.

# #### 2.4.1 Gauss-Seidel Method

# A modest upgrade is made accomplished through the Gauss-Seidel method, which uses a different way to iterate over the grid of points, taking into consideration the new information computed for the current iteration $n+1$ as follows:

# $$V_{n+1}(i,j)=\frac{1}{4}\left[V_{n}(i+1,j) +V_{n+1}(i-1,j)+ V_{n}(i,j+1)+V_{n+1}(i,j-1) \right]$$

# Even though there is an upgrade in performance, it only reduces the required iterations by a factor of 2.

# %%
#Definition of the relaxation method
def RelaxationGS(V, indicesCC, err=1e-7, imax=1000):
    
    diff = 0.; iteracion=0   # Initialize
    V_next = V_old = V.copy()
    nx = np.shape(V)[1]; ny = np.shape(V)[0]
    error = []

    # The while loop implements the Jacobi method
    while True:
        
        # The limits of the for loop take away the edges so each point has neighbors
        for i in range(1, nx-1):
            for j in range(1, ny-1):
                # Points non difined by CC are filtered
                if (i, j) in indicesCC:
                    pass
                else: 
                    tmp= V_old[i, j]
                    # Average
                    V_next[i, j] = (V_next[i+1, j] + V_next[i-1, j] + V_next[i, j+1] + V_next[i, j-1])/4.
                    #print(V_next[i, j], V_old[i, j])
                    # Each point contributes with error, which is summed
                    diff += np.abs( tmp - V_next[i, j])
        diff /= (2* nx +1)**2
        error.append(diff)
        
        iteracion += 1
        if diff< err: # Has the minimum error tolerance been achieved?
            print("Minimum error achieved. Err:", diff)
            break
            
        elif iteracion >= imax: # Has the maximum number of iterations been reached?
            print("Maximum number of iterations reached:", iteracion)
            break
            
        else:
            # Redefinition of arrays, the new is the old in the next iteration
            V_old = V_next.copy()
        
    return V_next, iteracion, diff, error

V_next, iteracion, diff, error_gs = RelaxationGS(V, indicesCC)

PlotVPotential(X, Y, V_next, "Capacitor of Parallel Plates")

