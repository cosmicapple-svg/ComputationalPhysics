# #### 2.4.2 Successive Over-Relaxation (SOR)

# Let $V^* (i, j)$ be the computed value by Jacobi's (or Gauss-Seidel) method, we can think of the quantity

# $$\Delta V(i, j) = V^*(i, j) - V_{n}(i, j)$$

# as the change recommended by Jacobi's (or Gauss-Seidel) method. This quantity is very conservative, such that to accelerate the convergence we can change the potential for

# $$V_{n+1}(i, j) =  \alpha \Delta(i, j) + V_{n}(i, j)$$

# where $\alpha$ is a factor that measure how much to over-relax:

# * $\alpha = 1 \qquad \implies$ Jacobi's Method

# * $\alpha\geq 2 \qquad \implies $ No convergence

# * $\alpha \in [1, 2] \qquad \implies$ Over-Relaxation

# The goal is to determine $\alpha$ such that a greater convergence is achieved, this value depends on the chosen geometry, but for an square grid the best choice is

# $$\alpha \approx \dfrac{2}{1 + \pi/L}$$

# This method makes the number of iterations of the order of $L$.

# %%
#Definition of the relaxation method
def RelaxationSOR(V, indicesCC, err=1e-7, imax=1000):
    
    errores=[]; iteracion=0   # Initialize
    V_next = V_GS= V.copy()
    ny, nx = np.shape(V)
    alpha = 2/(1 + np.pi/nx)
    error_list = []

    # The while loop implements the Jacobi method
    while True:
        
        # The limits of the for loop take away the edges so each point has neighbors
        for i in range(1, nx-1):
            for j in range(1, ny-1):
                # Points non difined by CC are filtered
                if (i, j) in indicesCC:
                    pass
                else: 
                    tmp = V_next[i, j]
                    V_GS = (V_next[i+1, j] + V_next[i-1, j] + V_next[i, j+1] + V_next[i, j-1])/4. # Gauss-Seidel
                    ajuste = alpha*(V_GS - tmp) # Adjustment by SOR method
                    V_next[i, j] = ajuste + tmp  # SOR
                    # Error by point is saved
                    error =abs(1 - V_GS/(V_next[i,j]+1e-12))
                    errores.append(error)
        
        errmax = max(errores)
        error_list.append(errmax)
        iteracion += 1
        if errmax < err: # Has the minimum error tolerance been achieved?
            print("Minimum error achieved. Err:", errmax)
            print("Iteration: ", iteracion)
            break
            
        elif iteracion >= imax: # Has the maximum number of iterations been reached?
            print("Maximum number of iterations reached:", iteracion)
            break
            
        else:
            # Restart
            errores = []
        
    return V_next, iteracion, diff, error_list

V_next, iteracion, diff, error_sor = RelaxationSOR(V, indicesCC)

PlotVPotential(X, Y, V_next, "Capacitor of Parallel Plates")

