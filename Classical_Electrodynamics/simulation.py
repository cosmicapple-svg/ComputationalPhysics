# ## 2. Laplace's Equation

# An interesting case to study is at regions in space where there are no electric charges where the differential Gauss' Law takes the shape

# $$\vec{\nabla}\cdot \vec{E}(x,y,z)= \frac{\partial E_x}{\partial x} + \frac{\partial E_y}{\partial y} + \frac{\partial E_z}{\partial z}=0$$

# And it is known that the electric field is the gradient of electric potential. Which indicates that the electric potential obeys Laplace's Equation

# $$\vec{\nabla}^2V = \frac{\partial^2V}{\partial x^2} + \frac{\partial^2V}{\partial y^2} + \frac{\partial^2V}{\partial z^2}=0$$

# Facing a situation that involves Laplace's equation, it is common to know certain frontier conditions for $V$ in a three dimensional surface, either its value or the value of its spatial derivative (electric field). Furthermore, due to the unicity theorem, it is known that by knowing the frontier conditions over a given closed surface for this system, the solution to Laplace's equation is unique and the potential and electric field are determined at each point of space.

# A key difference of these kind of problems is that this differential equation can not be solved with any of the Runge-Kutta methods since it is a partial differential equation. For this case, the **relaxation method** is applied, usefull for partial differential equations known as eliptic equation.

# For this case, the independent variables are discretized in a grid where each point is labeled by the whole numbers $i$, $j$ and $k$. In this way the objective is to determine $V(i,j,k)=V(i\Delta x,j\Delta y, k\Delta z)$. So the partial derivatives can be rewritten as 

# $$\begin{aligned}
#     \frac{\partial^2 V}{\partial x^2} & \approx \frac{1}{\Delta x} \left[\frac{V(i+1, j,k)-V(i,j,k)}{\Delta x}- \frac{V(i,j,k)-V(i-1,j,k)}{\Delta x}\right]\\
#     & \approx \frac{V(i+1, j,k)+V(i-1,j,k) -2V(i,j,k)}{(\Delta x)^2}
# \end{aligned}$$

# This expression is symmetric since it takes a point before and after to compute the derivative, which reduces the computational errors. The other partial derivatives are similar. Replacing in Lapace's equation and solving for $V(i,j,k)$ it is found

# $$\begin{aligned}
#     V(i,j,k)= & \frac{1}{6}[V(i+1,j,k)+V(i-1,j,k) +V(i,j+1,k) \\ & +V(i,j-1,k) +V(i,j,k+1)+V(i,j,k-1)]
# \end{aligned}$$

# where it is assumed that the step size in all directions is the same ($\Delta x=\Delta y=\Delta z$). This tells us that the value of the potential at any point is the average of $V$ over all the neighbouring points. The solution for $V(i,j,k)$ is the function that satisfies this condition simultaneously at all points.

# A way to determine this function assuming that $V$ is known at the frontiers is to take an initial approximation $V_0(i,j,k)$ and use it to compute a new upgraded value $V_1$, which will be used to compute a new value $V_2$, and repeat iteratively until reaching a result that satisfies a convergence criteria. This algorithm is known as Jacobi's method, even though there are other ways to implement the relaxation methods.

# ### 2.1 Electric Potential in a Square box

# Suppose an square box which is infinitely large in both directions of z axis, subject to the following frontier conditions

# ![](figures/Caja.png)

# 1. El potencial en su extremo izquierdo es -1, mientras que en su extremo derecho es 1:
# $$V(-1,y)=-1\qquad ,\qquad V(1,y)=1$$
# 2. El potencial en sus extremos superior e inferior sigue una relación lineal de tal modo que
# $$V(x,-1)=V(x,1)=x$$

