# # 2. Magnetic Fields

# Magnetic fields are produced around moving charges (currents or point charges). The simplest case is that of a steady current $I$ in a straight wire with a segment $d\vec{z}$. The magnetic field is obtained through the Biot-Savart Law

# $$d\vec{B}=\frac{\mu_0I}{4\pi} \frac{d\vec{z}\times\vec{z}}{r^3}$$

# where $\vec{r}$ is the vector pointing from the current-carrying segment to the point in question, and $\mu_0$ is the magnetic permeability of free space.

# ![](figures/Corriente_Cable.png)

# For this type of geometry, the cross product can be expressed in terms of the angle $\theta$ between $d\vec{z}$ and $\vec{r}$:

# $$dB=\frac{\mu_0I}{4\pi}\frac{dz\sin{\theta}}{r^2}$$

# The total field is the integral of $dB$ over the entire length of the wire. However, numerically, the integral cannot be performed directly; instead, a discrete sum of values is used by taking small intervals $dz\mapsto\Delta z$. That is, the field is:

# $$B\approx\sum_i\frac{\mu_0I}{4\pi}\frac{x\Delta z}{(z_i^2+x^2)^{3/2}}$$

# Various methods exist to improve the convergence and precision of numerical integrals, such as Simpson's Rule. As before, reducing the spacing size $\Delta z$ improves the results of the method.

# In this case, if the wire is infinitely long, the field is $B = \mu_0 I / (2\pi r)$.

# ## 2.1 Magnetic Dipole

# As is known from Gauss's Law for magnetism, magnetic monopoles do not exist in nature; the trajectories followed by magnetic field lines are always closed loops.


# However, magnetic multipoles do exist, with the dipole being the simplest. It possesses a "north" from which field lines emerge and a "south" toward which field lines are directed.

# In Python, using a code similar to the one used for electric potential, a magnetic dipole can be found numerically. This is because the magnetic field has the characteristic of rotating around its sources (moving charges or steady electric currents), distributing itself in space in a manner similar to the equipotential surfaces of an electric field.

