# ## Computational Simulation

# To solve the computational problem of finding the equipotential lines, we must discretize the continuous field line through a finite number of small line segments $\Delta l$ in the electric field direction, from which the following components can be derived:

# $$\Delta x=\Delta l\dfrac{E_x}{E} \qquad , \qquad \Delta y=\Delta l\frac{E_y}{E}$$

# where $E$ is the field's magnitude.

# ![](figures/Field_Lines.png)

# To compute the equipotential lines we use the fact that they are perpendicular to the electric field at each point. This tells us that if the field goes in the $(\Delta x,\Delta y)$ direction, the perpendicular direction is $(-\Delta y,\Delta x)$. Therefore, the equipotential lines can be obtained with:

# $$\Delta x=-\Delta l\dfrac{E_y}{E} \qquad , \qquad \Delta y=\Delta l\frac{E_x}{E}$$

# The algorithm to compute this is as follows:

# * 1. Choose an initial point $(x_0, y_0)$ which belongs to a single line to draw.

# * 2. Compute E at that point.

# * 3. Use an small $\Delta l$ and compute $\Delta x$ ans $\Delta y$. 

# * 4. Make a substitution $x \to x + \Delta x$ and $y \to y + \Delta y$. 

# * 5. Repeat until reaching a termination criteria, for example, when a field line reaches a given plotting area or gets to a minimum distance from the charge.

