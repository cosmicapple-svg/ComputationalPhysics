# %%
def EField(V_potential):
    # Electric field (x direction)
    nx = len(V_potential[0, :]); ny = len(V_potential[:, 0])
    
    Ex = np.zeros([nx, nx]); Ey = np.zeros([ny, ny])
    # Compute derivatives with finite differences centered
    for i in range(1, nx-1):
        for j in range(1, ny-1):
            Ex[i, j] = -(V_potential[i+1, j] - V_potential[i-1, j])/(2*(2/nx)) 
            Ey[i, j] = -(V_potential[i, j+1] - V_potential[i, j-1])/(2*(2/ny)) 
    
    return Ex, Ey

